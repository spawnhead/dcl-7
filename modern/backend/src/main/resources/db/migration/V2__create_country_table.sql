CREATE TABLE IF NOT EXISTS dcl_country (
  cut_id INTEGER PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
  cut_create_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  usr_id_create INTEGER NOT NULL,
  cut_edit_date TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  usr_id_edit INTEGER NOT NULL,
  cut_name VARCHAR(50) NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_dcl_country_name ON dcl_country(cut_name);
